//if-else
let age = 18;

if(age >= 18){
    console.log('You are allowed to drive a car');
}
else{
    console.log('You are not allowed to drive a car')
}

//if-elseif

let team = 'blue';

if(team == 'red'){
    console.log('You are on Red team')
}
else if(team == 'blue'){
    console.log('You are on Blue team')
}
else if(team == 'green'){
    console.log('You are on Green team')
}
else{
    console.log('Team not found')
}