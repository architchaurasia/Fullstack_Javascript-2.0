// Switch Case

let teacher = 'aman';

switch (teacher) {
    case 'aman':
        console.log(`${teacher} teacher of class 1st`);
        break;
    case 'meena':
        console.log(`${teacher} teacher of class 2nd`);
        break;
        case 'Shyaam':
            console.log(`${teacher} teacher of class 3rd`);
            break;
        default:
            console.log('Teacher not found');
}